import { registerEnumType } from 'type-graphql';

////////////////////////////////////////////////////////////////////////////////
// Enums
////////////////////////////////////////////////////////////////////////////////
export enum UnitOfMeasure {
  GRAM = 'gram',
}
////////////////////////////////////////////////////////////////////////////////
// Register enums with type-graphql
////////////////////////////////////////////////////////////////////////////////
export function register() {
  registerEnumType(UnitOfMeasure, { name: 'UnitOfMeasure' });
}
